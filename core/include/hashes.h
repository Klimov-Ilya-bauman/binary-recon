#ifndef HASHES_H
#define HASHES_H

#include <string>
#include <cstdint>
#include <cstddef>

/**
 * Криптографические хеш-функции MD5 и SHA-256.
 *
 * Обе реализованы с нуля по официальным спецификациям:
 *   - MD5    — RFC 1321 (Rivest, 1992)
 *   - SHA-256 — FIPS 180-4 (NIST, 2015)
 *
 * Обе функции построены по схеме Меркла-Дамгарда:
 *   1. Padding: дополняем входные данные до длины, кратной размеру блока.
 *   2. Initialize: задаём начальное состояние (initialization vector).
 *   3. Process blocks: для каждого блока обновляем состояние через
 *      многораундовую compression function.
 *   4. Finalize: текущее состояние и есть итоговый хеш.
 *
 * Свойства, ожидаемые от криптографической хеш-функции:
 *   - Детерминированность: одинаковый вход → одинаковый выход.
 *   - Лавинный эффект: смена 1 бита входа меняет ~50% бит выхода.
 *   - Необратимость: по хешу не восстановить оригинал.
 *   - Стойкость к коллизиям: трудно найти X, Y такие что hash(X) = hash(Y).
 *
 * MD5 в 2026 году считается КРИПТОГРАФИЧЕСКИ СЛОМАННЫМ:
 *   практические коллизии находятся за секунды на обычном ПК.
 *   Используется только для идентификации файлов (не для безопасности).
 * SHA-256 на 2026 год криптографически стоек.
 */
class Hashes {
public:
    /**
     * Вычисляет MD5 хеш произвольных данных.
     * @return 32-символьная hex-строка в нижнем регистре.
     *         Например: "d41d8cd98f00b204e9800998ecf8427e" (для пустого ввода).
     */
    static std::string md5(const uint8_t* data, size_t length);

    /**
     * Вычисляет SHA-256 хеш произвольных данных.
     * @return 64-символьная hex-строка в нижнем регистре.
     */
    static std::string sha256(const uint8_t* data, size_t length);
};

#endif // HASHES_H
